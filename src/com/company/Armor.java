package com.company;

public class Armor {

    private String name;
    private int power;
    private int stamina;
    private int agility;
    private int intellect;
    private int worth;

    public Armor(String name, int power, int stamina, int agility, int intellect, int worth) {
        this.name = name;
        this.power = power;
        this.stamina = stamina;
        this.agility = agility;
        this.intellect = intellect;
        this.worth = worth;
    }
}
